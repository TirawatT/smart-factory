export function getDeviceById(id: string): Device | undefined {
  return mockDevices.find((d) => d.id === id);
}

export function getDevicesByZone(zone: string): Device[] {
  return mockDevices.filter((d) => d.zone === zone);
}

export function getDevicesByStatus(status: string): Device[] {
  return mockDevices.filter((d) => d.status === status);
}

export function getDeviceStats() {
  return {
    total: mockDevices.length,
    online: mockDevices.filter((d) => d.status === "online").length,
    offline: mockDevices.filter((d) => d.status === "offline").length,
    error: mockDevices.filter((d) => d.status === "error").length,
    maintenance: mockDevices.filter((d) => d.status === "maintenance").length,
  };
}
